export function subKeyword(
  service: { shortName: string },
  area: Area
): string {
  const base = `${service.shortName} ${area.name}`;
  return area.subPos === "pre" ? `${area.sub} ${base}` : `${base} ${area.sub}`;
}

// Deterministic variant selection: the same area always resolves to the same
// wording for a given section, but different areas (and different sections)
// spread across the whole pool. This gives every generated page a genuinely
// different text mix to avoid duplicate-content SEO penalties across areas.
function hashString(input: string): number {
  let hash = 5381;
  for (let i = 0; i < input.length; i++) {
    hash = ((hash << 5) + hash + input.charCodeAt(i)) | 0;
  }
  return Math.abs(hash);
}

export function pickVariant<T>(area: string, section: string, variants: T[]): T {
  return variants[hashString(`${area}::${section}`) % variants.length];
}

