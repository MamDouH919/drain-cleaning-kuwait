export const blogPostBySlug = new Map(blogPosts.map((p) => [p.slug, p]));

export type BlogSummary = Pick<
  BlogPost,
  "slug" | "title" | "excerpt" | "category" | "cover" | "datePublished" | "readingMinutes"
>;

export function getBlogSummaries(): BlogSummary[] {
  return [...blogPosts]
    .sort((a, b) => (a.datePublished < b.datePublished ? 1 : -1))
    .map(({ slug, title, excerpt, category, cover, datePublished, readingMinutes }) => ({
      slug,
      title,
      excerpt,
      category,
      cover,
      datePublished,
      readingMinutes,
    }));
}

export function getRelatedPosts(slug: string, limit = 3): BlogSummary[] {
  const current = blogPostBySlug.get(slug);
  if (!current) return [];
  const sameCategory = blogPosts.filter(
    (p) => p.slug !== slug && p.category === current.category
  );
  const others = blogPosts.filter(
    (p) => p.slug !== slug && p.category !== current.category
  );
  return [...sameCategory, ...others].slice(0, limit).map((p) => ({
    slug: p.slug,
    title: p.title,
    excerpt: p.excerpt,
    category: p.category,
    cover: p.cover,
    datePublished: p.datePublished,
    readingMinutes: p.readingMinutes,
  }));
}
